class ProgramModel {
  final String programName;
  final String goodDrugName;
  final String officialDrugName;
  final String form;
  final String size;
  final String inPackSize;
  final String frequency;
  final String when;
  final String quaForOne;
  final String dateOf;
  final String beginDate;
  final String endDate;
  final String description;

  const ProgramModel({
    required this.programName,
    required this.goodDrugName,
    required this.officialDrugName,
    required this.form,
    required this.size,
    required this.inPackSize,
    required this.frequency,
    required this.when,
    required this.quaForOne,
    required this.dateOf,
    required this.beginDate,
    required this.endDate,
    required this.description,
  });
}